//Overloading (Nạp chồng):
//- Khái niệm: Định nghĩa nhiều phương thức cùng tên nhưng khác tham số trong cùng một lớp.
//- Thời điểm quyết định: Xác định tại thời điểm biên dịch (Compile-time).
//- Sự liên quan đến đối tượng: Chỉ xảy ra trong cùng một lớp.
//- Số lượng tham số: Phải khác nhau về số lượng hoặc kiểu dữ liệu tham số.
//- Mục đích sử dụng: Cung cấp nhiều cách gọi phương thức với tham số khác nhau.

//Overriding (Ghi đè):
//Khái niệm: Định nghĩa lại phương thức của lớp cha trong lớp con với cùng tên, cùng tham số.
//Thời điểm quyết định: Xác định tại thời điểm chạy (Runtime).
//Sự liên quan đến đối tượng: Xảy ra giữa lớp cha và lớp con.
//Số lượng tham số: Giữ nguyên số lượng và kiểu dữ liệu tham số.
//Mục đích sử dụng: Tùy chỉnh hành vi của phương thức trong lớp con.